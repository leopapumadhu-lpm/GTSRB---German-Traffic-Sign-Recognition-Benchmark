    img = image.resize((30, 30))
    img_array = np.array(img)/255.0
    img_array = np.expand_dims(img_array, axis=0)

    predictions = model.predict(img_array)
    pred_class = np.argmax(predictions)
    pred_prob = np.max(predictions)

    meaning = class_names.get(pred_class, {"en": "Unknown", "hi": "рдЕрдЬреНрдЮрд╛рдд", "ta": "роЕро▒ро┐ропрокрпНрокроЯро╛родродрпБ"})

    st.markdown("### Meaning of Traffic Sign:")
    st.write(f"**English:** {meaning['en']}")
    st.write(f"**Hindi:** {meaning['hi']}")
    st.write(f"**Tamil:** {meaning['ta']}")

    # Show prediction statistics
    st.markdown("### Prediction Probabilities for All Classes:")
