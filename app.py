# ---------------- APP LAYOUT ----------------
st.title("ЁЯЪж GTSRB Traffic Sign Recognition with Voice & Multi-Language")

mode = st.radio("Select Input Mode:", ["Upload Image", "Use Webcam"])

# ---------------- IMAGE INPUT ----------------
if mode == "Upload Image":
    uploaded_file = st.file_uploader("Upload a traffic sign image", type=["jpg", "png", "jpeg"])
    if uploaded_file:
        image = Image.open(uploaded_file)
        st.image(image, caption='Uploaded Image', use_column_width=True)

elif mode == "Use Webcam":
    st.info("Click 'Capture' to take a picture from your webcam.")
    run_webcam = st.button("Capture from Webcam")
    if run_webcam:
        cap = cv2.VideoCapture(0)
        ret, frame = cap.read()
        cap.release()
        if ret:
            frame = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
            image = Image.fromarray(frame)
            st.image(image, caption='Captured Image', use_column_width=True)
        else:
            st.error("Could not access webcam.")

# ---------------- PREDICTION ----------------
